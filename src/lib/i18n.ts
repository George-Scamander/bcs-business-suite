type TranslationTree = Record<string, unknown>

function isObject(value: unknown): value is TranslationTree {
  return typeof value === 'object' && value !== null && !Array.isArray(value)
}

function deepMerge(base: TranslationTree, patch: TranslationTree): TranslationTree {
  const merged: TranslationTree = { ...base }

  Object.entries(patch).forEach(([key, value]) => {
    if (isObject(value) && isObject(merged[key])) {
      merged[key] = deepMerge(merged[key] as TranslationTree, value)
      return
    }

    merged[key] = value
  })

  return merged
}

function deepClone<T>(value: T): T {
  return JSON.parse(JSON.stringify(value)) as T
}

