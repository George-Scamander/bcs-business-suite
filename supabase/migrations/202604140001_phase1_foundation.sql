create or replace function public.assign_role_by_email(p_email text, p_role_code text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid;
  v_role_id bigint;
begin
  select id into v_user_id
  from public.profiles
  where lower(email) = lower(p_email)
  limit 1;

  if v_user_id is null then
    raise exception 'Profile not found for email: %', p_email;
  end if;

  select id into v_role_id
  from public.roles
  where code = p_role_code
  limit 1;

  if v_role_id is null then
    raise exception 'Role not found for code: %', p_role_code;
  end if;

  insert into public.user_role_relations (user_id, role_id)
  values (v_user_id, v_role_id)
  on conflict (user_id, role_id) do nothing;
end;
$$;

