-- Auto profile bootstrap on auth user creation
create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_default_role_id bigint;
begin
  insert into public.profiles (id, email, full_name)
  values (new.id, coalesce(new.email, ''), new.raw_user_meta_data ->> 'full_name')
  on conflict (id) do update
    set email = excluded.email,
        full_name = coalesce(excluded.full_name, public.profiles.full_name);

  select id into v_default_role_id from public.roles where code = 'bd_user' limit 1;

  if v_default_role_id is not null then
    insert into public.user_role_relations (user_id, role_id)
    values (new.id, v_default_role_id)
    on conflict (user_id, role_id) do nothing;
  end if;

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_auth_user();

-- RBAC helper functions
create or replace function public.is_super_admin(target_user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_role_relations urr
    join public.roles r on r.id = urr.role_id
    where urr.user_id = coalesce(target_user_id, auth.uid())
      and r.code = 'super_admin'
  );
$$;

create or replace function public.has_permission(permission_code text, target_user_id uuid default auth.uid())
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.user_role_relations urr
    join public.role_permission_relations rpr on rpr.role_id = urr.role_id
    join public.permissions p on p.id = rpr.permission_id
    where urr.user_id = coalesce(target_user_id, auth.uid())
      and p.code = permission_code
  )
  or public.is_super_admin(target_user_id);
$$;

